# In the Day-2 of python Coding. We will learn how to use print() function to print other datatypes like integer, float, and bollen, etc.
# How to print integer using print() function in python.
# For positive integer
print(2) # Output: 2

# For negative integer
print(-4) # Output:-4

# How to print the float using print() function in python.
# for positive float
print(12.6) # Output: 12.6

# for negative float
print(-9.9) # Output: -9.9

# How to print the boolean using the print() function in python.
print(True) # Output: True
print(False) # Output: False

# How to print list using print() function in python.
print([1,2,3,4,5,6]) # Output: [1,2,3,4,5,6]
print(type([1,2,3,4,5,6])) # Output: <class 'list'>

# How to print tuple using print() function in python.
print((2,4,6,8,10,12)) # Output: (2,4,6,8,10,12)
print(type((2,4,6,8,10,12))) # Output: <classs 'tuple'>

# How to print set using print() function in python.
print({1,3,5,7,9,11}) # Output: 1,3,5,7,9,11}
print(type({1,3,5,7,9,11})) # Output: <class 'set'>

# How to print dictionary using print() function in python.
print({'Name':'Arpita','Tiltle':'Sah'}) # Output: {'Name':'Arpita','Tiltle':'Sah'}
print(type({'Name':'Arpita','Tiltle':'Sah'})) # Output: <class 'dict'>

